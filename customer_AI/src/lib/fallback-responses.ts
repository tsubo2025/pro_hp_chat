// キーワードに基づいて応答を取得する関数
export function getFallbackResponse(query: string): string {
    // クエリを小文字に変換
    const lowerQuery = query.toLowerCase();

    // キーワードに一致する応答を検索
    for (const item of fallbackResponses) {
        // デフォルト応答（キーワードが空の場合）はスキップ
        if (item.keywords.length === 0) continue;

        // いずれかのキーワードが含まれているか確認
        const hasMatch = item.keywords.some(keyword =>
            lowerQuery.includes(keyword.toLowerCase())
        );

        if (hasMatch) {
            return item.response;
        }
    }

    // 一致するものがなければデフォルト応答を返す
    return fallbackResponses[fallbackResponses.length - 1].response;
}